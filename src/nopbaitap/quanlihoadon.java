    public void loadbang() {
        try {
            conn = cn.getConnection();
            int number;
            Vector row;
            String sql = "select * from HoaDon";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            tb.setRowCount(0);
            while (rs.next()) {
                row = new Vector();
                int i;
                for (i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                tb.addRow(row);
                qlsp.setModel(tb);
            }
            st.close();
            rs.close();
            conn.close();
        } catch (Exception e) {

        }
        //  ddaayd chu len text
        qlsp.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (qlsp.getSelectedRow() >= 0) {
                    txtma.setText(qlsp.getValueAt(qlsp.getSelectedRow(), 0) + "");
                    txtten.setText(qlsp.getValueAt(qlsp.getSelectedRow(), 1) + "");
                    txtsoluong.setText(qlsp.getValueAt(qlsp.getSelectedRow(), 2) + "");
                    txtgia.setText(qlsp.getValueAt(qlsp.getSelectedRow(), 3) + "");
                    txtmota.setText(qlsp.getValueAt(qlsp.getSelectedRow(), 4) + "");
                    txttongtien.setText(qlsp.getValueAt(qlsp.getSelectedRow(), 5) + "");

                }
            }
        });

    }

